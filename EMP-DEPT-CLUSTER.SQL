-- CREATE CLUSTER 
CREATE CLUSTER EMP_DEPT (DEPTNO NUMBER(5)) SIZE 8K ;

-- CREATE INDEX CLUSTER 

CREATE INDEX emp_dept_index 
   ON CLUSTER emp_dept 
;


-- CREATE HASH CLUSTER
CREATE CLUSTER CLU_EMP_DEPT_HASH
(DEPTNO NUMBER(5)) 
SIZE 8K HASHKEYS 100
HASH IS MOD (DEPTNO,100)
;


CREATE TABLE EMPL_INDEX_CLUS
CLUSTER EMP_DEPT (DEPTNO)
AS SELECT * FROM EMPL;

CREATE TABLE DEPT_INDEX_CLUS
CLUSTER EMP_DEPT(DEPTNO) 
AS SELECT * FROM DEPT;

CREATE TABLE EMPL_HASH_CLUS
CLUSTER CLU_EMP_DEPT_HASH (DEPTNO)
AS SELECT * FROM EMPL;

CREATE TABLE DEPT_HASH_CLUS
CLUSTER CLU_EMP_DEPT_HASH(DEPTNO) 
AS SELECT * FROM DEPT;